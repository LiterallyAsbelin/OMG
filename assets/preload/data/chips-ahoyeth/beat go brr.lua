

function onBeatHit()
    if curBeat > 3 and curBeat < 60 then
        triggerEvent('Add Camera Zoom', 0.03,0.04)
    end

    if curBeat > 75 and curBeat < 108 then
        triggerEvent('Add Camera Zoom', 0.03,0.04)
    end

    if curBeat > 115 and curBeat < 171 then
        triggerEvent('Add Camera Zoom', 0.03,0.04)
    end

    if curBeat > 187 and curBeat < 250 then
        triggerEvent('Add Camera Zoom', 0.03,0.04)
    end

end